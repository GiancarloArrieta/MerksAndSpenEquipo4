    private void btnGuardarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarPedidoActionPerformed
   if (jTableSolicitud.getRowCount() > 0) {
        try {
            crud.GenerarPedido(jTableSolicitud); // Llama al método para generar el pedido
            JOptionPane.showMessageDialog(null, "✅ Pedido guardado correctamente en la base de datos.");

            // Limpiar la tabla si todo salió bien
            DefaultTableModel modelo = (DefaultTableModel) jTableSolicitud.getModel();
            modelo.setRowCount(0);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "❌ Error al guardar el pedido: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(null, "❌ No hay artículos en la solicitud para guardar.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnGuardarPedidoActionPerformed

