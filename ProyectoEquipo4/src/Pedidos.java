    private void btnAgregarArticuloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarArticuloActionPerformed
                                                 
    try {
        if (txtIdPedido.getText().isEmpty() || txtidArticulo.getText().isEmpty() ||
            txtcantidad.getText().isEmpty() || txtidUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "❌ Error: Todos los campos deben contener valores numéricos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int idPedido = Integer.parseInt(txtIdPedido.getText());
        int idArticulo = Integer.parseInt(txtidArticulo.getText());
        int cantidad = Integer.parseInt(txtcantidad.getText());
        int idUsuario = Integer.parseInt(txtidUsuario.getText());

        crud.AgregarArticulo(idPedido, idArticulo, cantidad, idUsuario, jTableSolicitud);

        // Limpiar campos después de la acción
        txtIdPedido.setText("");
        txtidArticulo.setText("");
        txtcantidad.setText("");
        txtidUsuario.setText("");

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(null, "❌ Error: Ingresa solo números en los campos.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnAgregarArticuloActionPerformed

