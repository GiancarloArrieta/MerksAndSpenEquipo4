    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String nombre = txtNombreEliminar.getText();
        if(nombre.isEmpty()){
            JOptionPane.showMessageDialog(null, "ERROR: Es necesario ingresar un ID para eliminar un usuario." , "Eliminación de departamento", JOptionPane.QUESTION_MESSAGE);
            return;
        }
        String botones[] = {"Sí", "No"};
        int decision = JOptionPane.showOptionDialog(null, "¿Estás seguro de querer eliminar el artículo '" + nombre + "'?",
            "Eliminación de usuario", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, botones, botones[0]);
        if(decision == 0){
            boolean status = crud.eliminarArticulo(nombre);
            if(status){
                JOptionPane.showMessageDialog(null, "El usuario se eliminó exitosamente", "Eliminación de artículo", JOptionPane.INFORMATION_MESSAGE);
                txtNombreEliminar.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "ERROR: El usuario no fue eliminó debido a un error.", "Eliminación de artículo", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            return;
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

