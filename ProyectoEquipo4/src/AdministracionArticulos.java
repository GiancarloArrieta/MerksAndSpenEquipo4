    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String nombre = txtArticulo.getText();
        String categoria = (String)boxCategoriasRegistro.getSelectedItem();

        if(nombre.isEmpty()){
            JOptionPane.showMessageDialog(null, "ERROR: Todos los campos son obligatorios.", "Registro de departamento", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean status = crud.crearArticulo(nombre, categoria);

        if(status){
            JOptionPane.showMessageDialog(null, "El artículo se guardó exitosamente", "Registro de artículo", JOptionPane.INFORMATION_MESSAGE);
            txtArticulo.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "ERROR: El artículo no fue guardado debido a un error.", "Registro de artículo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

